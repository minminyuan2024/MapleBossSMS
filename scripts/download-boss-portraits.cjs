/**
 * Download boss page thumbnails from MapleStory Wiki (same source as /w/Bosses → each boss page).
 * Saves files under public/boss-portraits/ and writes src/data/boss-portrait-local.json
 *
 * Run from project root: node scripts/download-boss-portraits.cjs
 *
 * License: wiki content is typically CC BY-SA; attribute MapleStory Wiki in your app/README.
 */

const fs = require("fs");
const path = require("path");

if (process.env.MAPLE_WIKI_INSECURE_TLS === "1") {
  process.env.NODE_TLS_REJECT_UNAUTHORIZED = "0";
  console.warn(
    "[download-boss-portraits] TLS verification disabled (MAPLE_WIKI_INSECURE_TLS=1). Use only on trusted networks.\n",
  );
}

const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "public", "boss-portraits");
const MANIFEST = path.join(ROOT, "src", "data", "boss-portrait-local.json");
const BOSSES_JSON = path.join(ROOT, "src", "data", "major-bosses.json");

function wikiTitleFromSlug(slug) {
  try {
    return decodeURIComponent(String(slug).replace(/\+/g, " "));
  } catch {
    return String(slug);
  }
}

function safeFileStem(slug) {
  const d = wikiTitleFromSlug(slug);
  return d.replace(/\//g, "__").replace(/:/g, "_");
}

function extFromUrl(imageUrl) {
  try {
    const u = new URL(imageUrl);
    const m = u.pathname.match(/\.(png|jpe?g|gif|webp)$/i);
    return m ? m[0].toLowerCase() : ".png";
  } catch {
    return ".png";
  }
}

async function wikiQueryThumbnails(slugs) {
  const body = new URLSearchParams({
    action: "query",
    format: "json",
    prop: "pageimages",
    pithumbsize: "400",
    titles: slugs.map((s) => wikiTitleFromSlug(s)).join("|"),
  });

  const res = await fetch("https://maplestorywiki.net/api.php", {
    method: "POST",
    headers: {
      Accept: "application/json",
      "Content-Type": "application/x-www-form-urlencoded",
      "User-Agent": "MapleBossNotify/1.0 (portrait sync; +local project)",
    },
    body: body.toString(),
  });

  if (!res.ok) throw new Error(`Wiki API ${res.status}`);
  const json = await res.json();
  const pages = json.query?.pages ?? {};
  const byTitle = new Map();

  for (const page of Object.values(pages)) {
    if (!page.title || page.missing) continue;
    byTitle.set(page.title, page.thumbnail?.source ?? null);
  }

  const norm = (s) => s.replace(/_/g, " ").toLowerCase().trim();
  const out = new Map();

  for (const slug of slugs) {
    const requested = wikiTitleFromSlug(slug);
    let url = byTitle.get(requested) ?? null;
    if (url == null) {
      const nr = norm(requested);
      for (const [t, u] of byTitle) {
        if (norm(t) === nr) {
          url = u;
          break;
        }
      }
    }
    out.set(slug, url);
  }
  return out;
}

const CHUNK = 12;

async function main() {
  const raw = JSON.parse(fs.readFileSync(BOSSES_JSON, "utf8"));
  const slugs = [...new Set(raw.map((r) => r.wikiSlug).filter(Boolean))];

  fs.mkdirSync(OUT_DIR, { recursive: true });

  const manifest = {};
  let ok = 0;
  let missing = 0;
  let failed = 0;

  for (let i = 0; i < slugs.length; i += CHUNK) {
    const chunk = slugs.slice(i, i + CHUNK);
    const thumbs = await wikiQueryThumbnails(chunk);
    await new Promise((r) => setTimeout(r, 400));

    for (const slug of chunk) {
      const imageUrl = thumbs.get(slug);
      if (!imageUrl) {
        manifest[slug] = null;
        missing += 1;
        console.warn(`No thumbnail: ${slug}`);
        continue;
      }

      const ext = extFromUrl(imageUrl);
      const filename = `${safeFileStem(slug)}${ext}`;
      const diskPath = path.join(OUT_DIR, filename);

      try {
        const imgRes = await fetch(imageUrl, {
          headers: {
            "User-Agent": "MapleBossNotify/1.0 (portrait download)",
            Referer: "https://maplestorywiki.net/",
          },
        });
        if (!imgRes.ok) throw new Error(`GET image ${imgRes.status}`);
        const buf = Buffer.from(await imgRes.arrayBuffer());
        fs.writeFileSync(diskPath, buf);
        manifest[slug] = `/boss-portraits/${filename}`;
        ok += 1;
        console.log(`OK ${slug} -> ${filename} (${buf.length} bytes)`);
      } catch (e) {
        manifest[slug] = null;
        failed += 1;
        console.warn(`Download failed ${slug}:`, e.message);
      }
    }
  }

  fs.writeFileSync(MANIFEST, JSON.stringify(manifest, null, 2) + "\n", "utf8");

  fs.writeFileSync(
    path.join(OUT_DIR, "README.txt"),
    [
      "Boss thumbnails downloaded from https://maplestorywiki.net/ via the MediaWiki API",
      "(pageimages, same artwork as linked boss pages from the Major Bosses list).",
      "",
      "Please retain attribution to MapleStory Wiki and respect CC BY-SA where applicable.",
      "",
      `Generated by scripts/download-boss-portraits.cjs — ${new Date().toISOString()}`,
      "",
    ].join("\n"),
    "utf8",
  );

  console.log(`\nDone. Saved ${ok} images, ${missing} wiki-miss, ${failed} download errors.`);
  console.log(`Manifest: ${MANIFEST}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
